public class Forloop {

	public static void main(String[] args) {
	//for loop
		for(int i=1; i<=5;i++)

		{
			System.out.println("Avthar"+i);
		}
		System.out.println("--------------------------------------");
	//while loop
		int i=1;
		while(i<=10)
		{
			System.out.println("black"+i);
			i+=5;
		}
		System.out.println("end of era");
		System.out.println("------------------------------------");
	//Do While loop
		int j=1;
		do
		{
			System.out.println("KGF");
			j++;
		}
		while(j<=5);
		System.out.println("bro");

	}

}
